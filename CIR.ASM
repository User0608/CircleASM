	CORY2 DB "INGRESE Y2:","$"

	TAMRAD DB "INGRESE RADIO:","$"

	ETIQUETA LABEL  BYTE
			MAXIMO	 DB 20
			LONGITUD DB ?
			DATO	 DB 20 DUP("$") 

	X1 DW 340 ;coordenada x centro
	Y1 DW 240 ;coordenada x centro
	COLOR1 DB 0AH ;Color ...

	X2 DW 300;coordenada y centro	
	Y2 DW 240 ;coordenada y centro	
	COLOR2 DB 0EH ;Color ...	

	RADIO DW 10;Radio ...
	X DW ?
	Y DW ?
	XC DW ?
	YC DW ?
	MODE DB ?
	COLOR DB ? ;Color ...
	LASTKEY DB "5" 
